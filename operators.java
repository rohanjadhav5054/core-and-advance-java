class operators{
    public static void main(String args[]){
        //operators
        //Arithmatic operators
        //addition
        int num1 = 10;
        int num2 = 5;
        int result = num1 + num2;
        System.out.println("Addition result will be : "+result);//15
        //substraction
        int result1 = num1 - num2;
        System.out.println("Substraction result will be : "+result1);//5
        //multiplication
        int result2 = num1 * num2;
        System.out.println("Multiplication result will be : "+result2);//50
        //division
        int result3 = num1 / num2;
        System.out.println("Division result will be : "+result3);//2
        //modulo
        int result4 = num1 % num2;
        System.out.println("Remainders result will be : "+result4);//0
        //increment
        int num = 10;
        num++;
        System.out.println("Increment of number is : "+num);//11
        //Addition increment 
        int number = 5;
        number += 2;//number = number + 2
        System.out.println("Addition assignment of number is : "+number);//7
        //substraction assignment
        int number_ = 15;
        number_ -= 5;//number_ = number_ - 5
        System.out.println("Substraction Assignment result is : "+number_);//10
        //Multiplication assignment 
        int numberis = 20;
        numberis *= 5;
        System.out.println("Substraction assignment "+numberis);//100
        //Modulo assignment 
        int number_is = 15;
        number_is %= 5;
        System.out.println("Modulo assignment result is : "+number_is);//0
        //decrement
        int number_is_this = 6;
        number_is_this --;
        System.out.println("Decerment result is : "+number_is_this);//5

    }
}