import java.util.*;
public class LinkedListConcept {
    public static void main(String args[]){
        LinkedList<Integer> ll = new LinkedList<>();

        ll.add(2);
        ll.addLast(3);
        ll.addFirst(1);
        //it contains all linked list operations 
        System.out.println(ll);
    }
}

