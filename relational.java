public class relational {
    public static void main(String args[]){
        //operators
        //Relational operators
        
        // == equal to
        int a = 5;
        int b = 5;
        boolean result = a == b;
        System.out.println("result of equal to operator will be :"+result);//true

        // != not equal to
        int c = 10;
        int d = 15;
        boolean result1 = c != d;
        System.out.println("Result of != operstor of two numbers will be : "+result1);//true

        // > greater than 
        int e = 10;
        int f = 7;
        boolean result2 = e > f;
        System.out.println("Result of greater then operator between two numbers will be "+result2);////true

        // < less than 
        int g = 15;
        int h = 16;
        boolean result3 = g < h;
        System.out.println("Result of less than operator between two numbers will be : "+result3);//true

        //>= greater than equal to 
        int i  = 6;
        int j = 7;
        boolean result4 = j >= i;
        System.out.println("Result of greater than equal to operator will be :"+result4);//true

        //<= less than equal to 
        int k = 15;
        int l = 16;
        boolean result5 = l <= k;
        System.out.println("Result of less than equal to to the those variables will be :"+result5);//false
    }
}
